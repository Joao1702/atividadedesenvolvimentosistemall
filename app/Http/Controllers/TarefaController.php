    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tarefa = new TarefaModel();
        $tarefa->titulo = $request->input("titulo");
        $tarefa->usuario = $request->input("usuario");
        $tarefa->privacidade = $request->input("privacidade");
        $tarefa->descicao = $request->input("descicao");
        $tarefa->tipo = $request->input("tipo");
        $tarefa->status = $request->input("status");
        $tarefa->dataConclusao = $request->input("data");
        $tarefa->save();
        return redirect()->route('tarefa.create');
    }

